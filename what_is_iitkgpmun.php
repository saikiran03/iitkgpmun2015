<b>
desire to innovate
</b>
is at the core of our practices. It is this ideology that will be central to all our conferences. It is our hope that all the attendees of the conference, be it the Executive Board, the delegates, the Press, as well as the conference staff, are going to be enamoured by the concept of IITKGPMUN simply because of our principle of innovation in each and every aspect of our work.
</p>
<p>
While it is innovation that guides us, every one of us here at IITKGPMUN possesses an inherent, burning
<b>
