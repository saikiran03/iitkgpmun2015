Here at IITKGPMUN, we are continuously looking to outdo ourselves in our goal to attain perfection in all aspects of the Conference, from preparing the most well-informed and comprehensive study guides to penning the agendas, from preparing up-to-date research material to providing the best of conference logistics and more. It is this profound
<b>
dedication towards perfection
</b>
that defines who we are, and is the driving force behind all those man-hours of tireless work behind the scenes to provide for a memorable experience for one and all attached to the conference.
