<p>
A concept that was born over several cups of coffee on a warm summer night in April 2011, IIT Kharagpur Model United Nations Conference is an endeavour to reach out to the best minds in the country to find solutions to global issues. Organized by Spring Fest, the annual social and cultural festival of the institute, and Debating Society, IIT Kharagpur, we believe that at IITKGPMUN, borders will be transcended, obstacles tackled, crises handled effectively, and solutions will emerge; but most importantly, long-lasting friends will be made.
</p>
