<p>
Barath is currently a junior at IIT Kharagpur. Having been brought up in
 Bangalore and Chennai, he adds to the South Indian flavor of the team, 
what with his childhood obsession of reading The Hindu sipping ‘filter 
kaapi’ extending into his college days, much to the exasperation of his 
wing mates. An Organizing Head in the Events Team of Spring Fest, his 
interests primarily lie in ‘checking out’ new rock music (he has no 
qualms revealing that rocknewsdesk.com is his homepage). His other 
interests include writing, reading and photography.
</p>
