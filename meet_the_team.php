<div class="entry">
<p>
Prashanth is currently a junior at IIT Kharagpur. His passion for 
International politics and statecraft has seen him attend several Model 
UN conferences all over the country since his freshman year; he thrives 
off of it. In addition to being the Secretary-General, he actively 
contributes to Debating Society, IIT Kharagpur where he serves in the 
capacity of Governor and leads the inter-collegiate team in various 
debating events. Outside the class and conference room, he is a prolific
 athlete with an impressive record in sprinting and is also a trainee at
 the prestigious Indian Squash Academy. Known for his perfectionist 
demeanour and strategic planning, he hopes to provide you with a 
glitch-free conference and a unique and memorable Model UN experience.
</p>
