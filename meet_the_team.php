<div class="entry">
<p>
Adarsh, fondly called Matthews in a cruel, mangled version of his 
surname, is a junior currently pursuing Industrial Engineering and 
Management Sciences at IIT Kharagpur. While not optimizing supply chain 
systems, he can be found doing a myriad of stuff, both good and bad. An 
avid writer, a seasoned debater and a sports aficionado [famous for his 
scary ManUtd rants]; he's what you call a jack-of-all-trades. An 
assistant editor with the campus newspaper and a Governor of the Deb-Soc
 here, he claims the MUN International Press presents the perfect 
platform for him to pursue his journalistic passions on a formal level.
</p>
